def find_unknown_enum_attributes(raw_enums: list[dict[str, Any]]) -> set[str]:
    """Return attributes on enums that aren't on the known list."""
    known_attribs = set(EnumValue.__dataclass_fields__.keys())
    known_attribs.remove('groups')
    known_attribs.add('group') # XMLs use 'group', our EnumValue uses 'groups'.

    novel = set()
    for enum_dict in raw_enums:
        for attrib in enum_dict.keys():
            if attrib not in known_attribs:
                novel.add(attrib)

    return novel


def identify_enum_groups(enums: list[EnumValue]) -> dict[str, int]:
    """Gather all encountered group names and count their occurrences."""
    # TODO: perhaps just use Counter?
    result: dict[str, int] = defaultdict(int)

    for enum in enums:
        for group_name in enum.groups:
            result[group_name] += 1

    return result


def sort_enums_by_group(enums: list[EnumValue]) -> dict[str, list[EnumValue]]:
    """Return a dict with group name as key and enums from this group as value."""
    result: dict[str, list[EnumValue]] = defaultdict(list)

    for enum in enums:
        if not enum.groups:
            result[''].append(enum)
        for group_name in enum.groups:
            result[group_name].append(enum)

    return result


