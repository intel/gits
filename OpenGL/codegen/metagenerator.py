def two_way_list_diff(list_a: list[Any], list_b: list[Any]) -> tuple[list[Any], list[Any]]:
    """Return tuple of items only in list_a and items only in list_b."""
    set_a: set[Any] = set(list_a)
    set_b: set[Any] = set(list_b)
    # Using list.index as key preserves original order.
    # TODO: list.index has O(n) complexity, consider using a better method.
    only_in_set_a = sorted(set_a - set_b, key=list_a.index)
    only_in_set_b = sorted(set_b - set_a, key=list_b.index)
    return only_in_set_a, only_in_set_b


def diff_xml_and_generator_enums(
    xml_enums: dict[Api, list[EnumValue]],
    generator_enums: dict[Api, list[EnumValue]],
) -> None:
    """Diff enums from XML with those from legacy generator data."""
    only_in_xml: dict[Api, list[EnumValue]] = {}
    only_in_generator: dict[Api, list[EnumValue]] = {}

    for api in Api:
        xml_list: list[EnumValue] = xml_enums.get(api, [])
        generator_list: list[EnumValue] = generator_enums.get(api, [])

        enums_only_in_generator: list[EnumValue]
        enums_only_in_xml: list[EnumValue]
        enums_only_in_generator, enums_only_in_xml = two_way_list_diff(generator_list, xml_list)

        if enums_only_in_xml:
            only_in_xml[api] = enums_only_in_xml
        if enums_only_in_generator:
            only_in_generator[api] = enums_only_in_generator

    if only_in_xml:
        print("####### Enums only present in XML #######")
        print(mako_render('templates/meta_enums.mako', enums_by_api=only_in_xml))
    if only_in_generator:
        print("####### Enums only present in generator #######")
        print(mako_render('templates/meta_enums.mako', enums_by_api=only_in_generator))


