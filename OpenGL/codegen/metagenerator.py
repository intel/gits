def mako_render(inpath: str, **kwargs) -> str | None:
    """Render a Mako template into a string."""
    # Objects used by all (or almost all) Mako templates.
    common_objects = {
        'EnumType': EnumType,
        'EnumValue': EnumValue,
        'FuncType': FuncType,
        'Token': Token,
        'Argument': Argument,
        'ReturnValue': ReturnValue,
        'AUTO_GENERATED_HEADER': AUTO_GENERATED_HEADER,
    }

    try:
        print(f"Processing {inpath}...")
        template = mako.template.Template(filename=inpath)
        rendered = template.render(**(common_objects | kwargs))
        rendered = re.sub(r'\r\n', r'\n', rendered)
        return rendered
    except Exception:
        traceback = mako.exceptions.RichTraceback()
        for filename, lineno, function, line in traceback.traceback:
            print(f"{filename}({lineno}) : error in {function}")
            print(line, "\n")
        print(f"{traceback.error.__class__.__name__}: {traceback.error}")
        print(f"Could not render template {Path(inpath).name}, skipping.")
        return None


def mako_write(inpath: str, outpath: str, **kwargs) -> None:
    """Render a Mako template into a file."""
    rendered: str = mako_render(inpath, **kwargs)
    if rendered is None:
        return

    destination = Path('..') / outpath
    with destination.open('w') as fout:
        fout.write(rendered)


def get_enums_from_xml(paths_and_apis: dict[Path, Api]) -> dict[Api, list[EnumValue]]:
    """Load XML registry files, return a dict mapping each to a list of its enums."""
    missing_paths = [path for path in paths_and_apis if not path.is_file()]
    if missing_paths:
        paths = '\n'.join(f'  - {path}' for path in missing_paths)
        raise FileNotFoundError(
            'Missing registry XML files:\n'
            f'{paths}\n'
            'Ensure OpenGL and EGL registries are downloaded in third_party.'
        )

    raw_enums_by_api: dict[Api, list[dict[str, Any]]] = {api: [] for api in Api}

    for path, api in paths_and_apis.items():
        registry = Registry()
        registry.loadFile(str(path))

        raw_enums: list[dict[str, Any]]
        raw_enums = [dict(**e.elem.attrib) for e in registry.enumdict.values()]
        raw_enums_by_api[api] = raw_enums

        # Check for unknown enum attributes.
        novel: set[str] = find_unknown_enum_attributes(raw_enums)
        if novel:
            print(f'WARNING: Unknown enum attributes in {path}: {novel}')

    return get_enums_from_xml_data(raw_enums_by_api)


